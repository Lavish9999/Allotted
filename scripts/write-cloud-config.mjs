// Generates www/cloud-config.js (gitignored) from SUPABASE_URL and
// SUPABASE_ANON_KEY, read from the environment or a local .env file.
// Safe to run unconditionally (e.g. as a Codemagic step): when the vars are
// missing it exits 0 and the app simply stays in local mock/test mode.
// Usage: npm run cloud:config
import { readFileSync, writeFileSync, existsSync } from "node:fs";

function loadDotEnv() {
  if (!existsSync(".env")) return {};
  const out = {};
  for (const line of readFileSync(".env", "utf8").split("\n")) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
    if (m) out[m[1]] = m[2].replace(/^['"]|['"]$/g, "");
  }
  return out;
}

const env = { ...loadDotEnv(), ...process.env };
const url = (env.SUPABASE_URL || "").trim();
const key = (env.SUPABASE_ANON_KEY || "").trim();

if (!url || !key) {
  console.log("cloud:config - SUPABASE_URL / SUPABASE_ANON_KEY not set; skipping. App stays in local test mode.");
  process.exit(0);
}

if (!/^https:\/\/[a-z0-9-]+\.supabase\.(co|in)\b/i.test(url)) {
  console.warn("cloud:config - warning: SUPABASE_URL does not look like a Supabase project URL:", url);
}

// Refuse service_role keys outright. Supabase keys are JWTs whose payload
// carries a `role` claim; only the anon public key may ship in the app.
function jwtRole(jwt) {
  try {
    const payload = jwt.split(".")[1];
    const json = Buffer.from(payload.replace(/-/g, "+").replace(/_/g, "/"), "base64").toString("utf8");
    return JSON.parse(json).role || "";
  } catch {
    return "";
  }
}
const role = jwtRole(key);
if (role === "service_role") {
  console.error("cloud:config - REFUSED: that is a service_role key. It bypasses Row Level Security and must NEVER ship in the app. Use the anon public key (Settings -> API).");
  process.exit(1);
}
if (role && role !== "anon") {
  console.warn(`cloud:config - warning: key role is "${role}", expected "anon". Double-check you copied the anon public key.`);
}

const banner = "// GENERATED by scripts/write-cloud-config.mjs - DO NOT COMMIT.\n// This file is gitignored; npm test fails the build if it is ever tracked.\n";
const body = `window.ALLOTTED_CLOUD_CONFIG = ${JSON.stringify({ url, anonKey: key }, null, 2)};\n`;
writeFileSync("www/cloud-config.js", banner + body);
console.log("cloud:config - wrote www/cloud-config.js for", url, role ? `(key role: ${role})` : "");
